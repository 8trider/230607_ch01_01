# 주석 #을 붙이면 컴퓨터가 읽지 않는다. 프로그램 실행에 영향을 주지 않는다
# 컨트롤+슬래시
# 보통 주석은 코드에 대한 설명 혹은 특정 코드를 임시로 사용하지 않게 하는 용도
# 주석은 한줄 주석, 블록 주석 두 가지가 있다.
# 한 줄 주석
print("한 줄 주석") # 코드 뒤에 이어서 주석

# print("Hello World") #코드 뒤에서 컨트롤 슬래시 하면 코드 앞에 샾이 붙는다

# 블록 주석, 여러 줄 주석을 작성할 때 """ 혹은 '''을 사용
'''
print("Hello World")
print("Hello World")
print("Hello World")
'''

# 드래그하고 컨트롤 슬래시 해도 된다